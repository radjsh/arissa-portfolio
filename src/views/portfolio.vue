<script>
import PortfolioCategories from "../components/PortfolioCategories.vue";
import ContactMe from "../components/ContactMe.vue";

export default {
  created() {
    window.addEventListener('resize', this.checkScreen);
    this.checkScreen();
    window.addEventListener('scroll', this.reveal);
  },

  name: 'Portfolio',
  components: {
    PortfolioCategories,
    ContactMe
  },

  data () {
    return {
    mobileView: null,
    windowWidth: null,
    clickCount: 0,
    }
  },

  methods : {
    checkScreen () {
        this.windowWidth = window.innerWidth;
        if (this.windowWidth <= 750){
            this.mobileView= true;
            
            return;
        }
        this.mobileView = false;
    },

    reveal () {
      var reveals = document.querySelectorAll(".reveal")

      for (var i = 0; i < reveals.length; i++) {
          var windowHeight = window.innerHeight;
          var elementTop = reveals[i].getBoundingClientRect().top;
          var elementVisible = 200;

          if (elementTop < windowHeight - elementVisible) {
              reveals[i].classList.add("active");
          } else {
              reveals[i].classList.remove("active");
          }               
      }           
    },

    startWave() {
      const arm = document.getElementById('arm')
      const eyes = document.getElementById('eyes')
      const mouth = document.getElementById('mouth')
      const bubble = document.getElementById('bubble')
      const bubble2 = document.getElementById('bubble-2')

      console.log("bubble classList pre-wave: " + bubble.classList)
      console.log("bubble2 classList pre-wave: " + bubble2.classList)

      if(arm.classList.contains('wave') == false){
        arm.classList.add('wave')
        eyes.classList.add('wave')
        mouth.classList.add('wave')
        this.clickCount += 1;
      }

      console.log('clickCount: ' + this.clickCount)

      if(this.clickCount == 1) {
        console.log("HELLO BUBBLE APPEAR")
        bubble.classList.add('appear')
        console.log("bubble: " + bubble.classList)
      }
      if((this.clickCount % 3) == 0) {
        console.log("LONG BUBBLE APPEAR")
        bubble2.classList.add('appear')
        console.log("bubble-2: " + bubble2.classList)
      }

      setTimeout(() => {this.endWave();}, 2000);
      console.log("bubble classList post-wave: " + bubble.classList)
      console.log("bubble2 classList post-wave: " + bubble2.classList)

    },

    endWave() {
      const arm = document.getElementById('arm')
      const eyes = document.getElementById('eyes')
      const mouth = document.getElementById('mouth')
      const bubble = document.getElementById('bubble')
      const bubble2 = document.getElementById('bubble-2')
      arm.classList.remove('wave')
      eyes.classList.remove('wave')
      mouth.classList.remove('wave')
      if (bubble.classList.contains('appear')) {
        bubble.classList.remove('appear')
      }
      if (bubble2.classList.contains('appear')) {
        bubble2.classList.remove('appear')
      }
    },
  }
}
